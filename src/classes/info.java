package classes;

public class info {

	/**
	 * Save information coming from the classification of a test file
	 * 
	 */
	private String type;
	private String token;

	public info() {
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}

}
